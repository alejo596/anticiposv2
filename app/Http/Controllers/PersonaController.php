    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $persona=Persona::select('gestion_ordenes.proveedores.run','ssalud.persona.per_rut','ssalud.persona.per_nombre','gestion_ordenes.proveedores.id')
        ->join('gestion_ordenes.proveedores','ssalud.persona.per_rut','=','gestion_ordenes.proveedores.run')
        ->where('per_nombre','like','%'.$id.'%')
        ->OrWhere('per_rut','like','%'.$id.'%')
        ->get();
        return $persona;
    }

